let PROTO_PATH = __dirname + "/demo_bank.proto";
let packageDefinition = protoLoader.loadSync(PROTO_PATH, {
  keepCase: true,
  longs: String,
  enums: String,
  defaults: true,
  oneofs: true,
});
let api = grpc.loadPackageDefinition(packageDefinition).demobank.api;

